public class Star2 {
    public static void main(String[] args) {
        for(int i=0; i<5; i++)
        {
            switch(i) {
                case 0:
                    System.out.printf("*\n");

                case 1:
                    System.out.printf("**\n");

                case 2:
                    System.out.printf("***\n");

                case 3:
                    System.out.printf("****\n");

                case 4:
                    System.out.printf("*****\n");

            }
            break;
        }
    }
}
